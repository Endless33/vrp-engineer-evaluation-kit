package report

import (
	"fmt"
	"os"
	"time"

	"github.com/Endless33/vrp-engineer-evaluation-kit/internal/evaluator"
)

// Generator creates engineering evaluation reports.
type Generator struct{}

// New creates a new report generator.
func New() *Generator {
	return &Generator{}
}

// WriteMarkdown writes a simple engineering report.
func (g *Generator) WriteMarkdown(path string, result *evaluator.Result) error {
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	_, err = fmt.Fprintf(f, `# VRP Engineering Evaluation Report

Generated:
%s

## Summary

Status: %t

Message:
%s

## Timing

Started:
%s

Finished:
%s

Duration:
%s

---

This report was generated by the public VRP Engineer Evaluation Kit.

It contains engineering evaluation metadata only.

No protected runtime implementation, proprietary algorithms,
or confidential protocol logic are included.
`,
		time.Now().UTC().Format(time.RFC3339),
		result.Passed,
		result.Message,
		result.StartTime.UTC().Format(time.RFC3339),
		result.EndTime.UTC().Format(time.RFC3339),
		result.Duration.String(),
	)

	return err
}
