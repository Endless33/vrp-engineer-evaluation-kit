package report

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
	"time"

	"github.com/Endless33/vrp-engineer-evaluation-kit/internal/evaluator"
)

func TestNew(t *testing.T) {
	generator := New()

	if generator == nil {
		t.Fatal("expected generator")
	}
}

func TestWriteMarkdown(t *testing.T) {
	generator := New()

	result := &evaluator.Result{
		StartTime: time.Now().UTC(),
		EndTime:   time.Now().UTC().Add(2 * time.Second),
		Duration:  2 * time.Second,
		Passed:    true,
		Message:   "ENGINEERING_EVALUATION_COMPLETED",
	}

	output := filepath.Join(t.TempDir(), "report.md")

	if err := generator.WriteMarkdown(output, result); err != nil {
		t.Fatalf("WriteMarkdown failed: %v", err)
	}

	data, err := os.ReadFile(output)
	if err != nil {
		t.Fatalf("ReadFile failed: %v", err)
	}

	text := string(data)

	expected := []string{
		"# VRP Engineering Evaluation Report",
		"ENGINEERING_EVALUATION_COMPLETED",
		"Status: true",
		"This report was generated by the public VRP Engineer Evaluation Kit.",
	}

	for _, value := range expected {
		if !strings.Contains(text, value) {
			t.Fatalf("report does not contain %q", value)
		}
	}
}

func TestWriteMarkdownCreatesFile(t *testing.T) {
	generator := New()

	result := &evaluator.Result{
		StartTime: time.Now().UTC(),
		EndTime:   time.Now().UTC(),
		Duration:  time.Second,
		Passed:    true,
		Message:   "OK",
	}

	output := filepath.Join(t.TempDir(), "evaluation.md")

	if err := generator.WriteMarkdown(output, result); err != nil {
		t.Fatalf("WriteMarkdown failed: %v", err)
	}

	info, err := os.Stat(output)
	if err != nil {
		t.Fatalf("Stat failed: %v", err)
	}

	if info.Size() == 0 {
		t.Fatal("expected non-empty report")
	}
}